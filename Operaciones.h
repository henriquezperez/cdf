class Operaciones{
	
	private:
		int a;
		int b;
		int c;
	public:			
		int Suma(int a, int b);
		int Resta(int a, int b);
		int Multilicacion(int a, int b);
		int Divison(int a, int b);
};

Operaciones::Suma(int a, int b){
	return c = a + b;
}

Operaciones::Resta(int a, int b){
	return c = a - b;
}

Operaciones::Multilicacion(int a, int b){
	return c = a * b;
}

Operaciones::Divison(int a, int b){
	return c = a/b;
}
